function parseBlockDis(){
    let all_block_dis = [];
    for(let i = 0; i < Blocks.length; ++i){
        let tmp_block = Blocks[i];
        let block_dis = [];
        let curr_pos = null;
        for(let r = 0; r < tmp_block.length; ++r){
            let row = tmp_block[r];
            for(let c = 0; c < row.length; ++c){
                let tmp_b = row[c];
                if(tmp_b > 0){
                    if(curr_pos === null) {
                        curr_pos = [r, c];
                        block_dis.push([0,0]);
                    }
                    else{
                        block_dis.push([curr_pos[0] - r, c - curr_pos[1]]);
                    }
                }
            }
        }
        all_block_dis.push(block_dis);
    }
    console.log(all_block_dis);
}


